



def add(a,b):
    """
    Adds two numbers together
    
    Args:
        a: Int,Float First number to add
        b: Int,Float Second number to add
    
    Returns:
        Int/Float
    """
    sum_val = a + b
    return sum_val



def sub(a,b):
    """
    Returns the difference between two numbers
    
    Args:
        a: Int,Float First number to subtract from
        b: Int,Float Second number
    
    Returns:
        Int/Float
    """
    diff_val = a - b
    return diff_val



def mul(a,b):
    """
    Multiplies two numbers together
    
    Args:
        a: Int,Float First number 
        b: Int,Float Second number to multiply with
    
    Returns:
        Int/Float
    """
    mul_val = a * b
    return mul_val



def div(a,b):
    """
    Divides a with b
    
    Args:
        a: Int,Float First number
        b: Int,Float Second number to divide with
    
    Returns:
        Int/Float
    """
    div_val = a / b
    return div_val